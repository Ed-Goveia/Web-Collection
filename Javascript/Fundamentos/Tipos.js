/* Tipos em JavaScript

> O conjuntos de tipos na linguagem JavaScript consiste em 'primitive values' e 'objects'.

'Valores primitivos': são todos os tipos de dados, exceto objetos, definem valores imutáveis (ou seja, valores que não podem ser alterados).

- Tipo 

*/

// Operador 'typeof' retorna o tipo da variável.

let numero = 5436;
let texto = "Olá";
let indefinido;
let vazio = null;
let boleano = true;

console.log("'numero' é do tipo:",typeof(numero));
console.log("'texto' é do tipo:",typeof(texto));
console.log("'indefinido' é do tipo:",typeof(indefinido));
console.log("'vazio' é do tipo:",typeof(vazio));
console.log("'boleano' é do tipo:",typeof(boleano));

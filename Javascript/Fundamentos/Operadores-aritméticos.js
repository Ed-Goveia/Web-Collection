// Soma '+':

console.log("Soma =", 10 + 5);

// Divisão '/':

console.log("Divisão =", 10 / 5);

// Subtração '-':

console.log("Subtração =", 10 - 5);

// Potenciação '**':

console.log("Potenciação =", 10 ** 2);

// Multiplicação '*':

console.log("Multiplicação =", 10 * 5);

// Resto '%' - em operações como divisões não exatas, logo as que sobram valores após o calculo, pode-se usar este operador para saber o número restante:

console.log("Resto =", 10 % 3);

// Somar valor a variável '+=':

let var_x = 1; // Valor de origem atribuído 

var_x = var_x + 1; // maneira extensa
console.log("Valor após soma_a =", var_x);

var_x = 1; // Defini igual ao primeiro valor para exemplificar.

var_x += 1; // de maneira contraída:
console.log("Valor após soma_b =", var_x);

